import { callGemini } from "./gemini.js";

export interface RetouchInput {
  draft: string;
  blogType: "seo" | "review";
  persona: string;
  keyword: string;
}

const RETOUCH_PROMPT = `당신은 {persona}입니다.
아래 초안을 당신의 블로그에 올릴 글로 자연스럽게 다시 작성하세요.

[가장 중요한 규칙]
- 글 길이는 반드시 2500~3000자로 맞추세요. 이보다 짧거나 길면 안 됩니다.
- 초안이 3000자보다 길면 불필요한 반복이나 사족을 줄여서 3000자 이내로 맞추세요.
- 초안이 2500자보다 짧으면 내용을 보강해서 2500자 이상으로 맞추세요.
- 절대 요약하거나 내용을 빼먹지 마세요. 소제목(##)과 하위제목(###)은 빠짐없이 포함하세요.

[리터치 규칙]
1. AI가 쓴 느낌을 완전히 제거하세요
   - "~입니다", "~합니다" 반복 금지
   - "첫째, 둘째, 셋째" 같은 기계적 나열 금지
   - "다양한", "효과적인", "중요한" 같은 AI 상투어 제거

2. 네이버 블로그 말투로 바꾸세요
   - "~거든요", "~더라고요", "~했어요" 혼합 사용
   - 중간에 혼잣말 ("근데 이게 진짜 예쁜 게...") 삽입
   - 문장 길이를 불규칙하게 (짧은 문장 2~3개 + 긴 문장 1개)

3. 경험담을 구체적으로 보강하세요
   - 시간: "지난 주에", "작년 겨울에"
   - 장소: "강남역 근처", "회사 앞"
   - 감정: "진짜 고민 많이 했는데", "보자마자 마음에 들었어요"
   - 디테일: 가격대, 배송 시간, 포장 상태 등

4. 글 구조를 블로그답게 바꾸세요
   - 도입: 일상적인 상황으로 시작 (절대 키워드 설명으로 시작 X)
   - 중간: 자연스럽게 정보 전달
   - 마무리: "~해보세요!" 같은 권유형 (요약 정리 X)

5. 네이버 블로그 SEO에 최적화하세요
   - 키워드 "{keyword}"를 제목, 인트로, 대표주제(첫 ##), 본문에 자연스럽게 반복 배치 (3~5회)
   - 키워드를 억지로 끼워넣지 말고 문맥에 맞게 자연스럽게 녹여주세요
   - 소제목(##)에도 키워드를 1~2개 포함시키세요
   - 첫 2~3문장 안에 키워드가 반드시 들어가야 해요

5. 아래 표현은 절대 사용 금지
   - "오늘은 ~에 대해 알아보겠습니다"
   - "~하는 것이 중요합니다"
   - "마지막으로 정리하자면"
   - "참고하시기 바랍니다"
   - "도움이 되셨으면 좋겠습니다"

6. [IMAGE_MID], [IMAGE_BOTTOM] 마커는 반드시 유지하세요. [IMAGE_TOP]은 사용 금지.

7. 제목도 다시 작성하세요 (25자 이하, 키워드 "{keyword}" 맨 앞).
   - 제목, 소제목, 본문 어디에도 페르소나 이름/닉네임/가게 이름 절대 넣지 않기 (예: 플로라, 꽃친 등 금지)
   - "안녕하세요" 같은 인사말 넣지 않기
   - "~'s 픽", "~가 알려주는", "~년차 꽃집 언니", "~년차 사장", "~의 꿀팁" 같은 페르소나/경력 표현 제목에 절대 금지
   - 제목은 순수하게 키워드 + 정보 느낌으로만 (예: "개업 화환 보내는 법, 헷갈리면 이걸 보세요!")

8. 서식 구조를 반드시 유지하세요 — 이것이 가장 중요합니다!
   - "# 제목" 형식 유지
   - "## 소제목" 형식 유지 (** 볼드로 바꾸지 않기! 반드시 ## 사용)
   - "### 이모지 하위제목" 형식 유지 (예: "### ⏰ 배송 시간은 여유 있게!")
   - 이모지가 있는 항목/분류는 반드시 ### 형식으로 유지하세요. 일반 본문으로 바꾸지 마세요!
   - ## 소제목에는 이모지를 넣지 마세요! (### 하위제목에만 이모지 사용)
   - "플로라의 한마디", "플로라 추천" 같은 페르소나 표현 절대 금지 — "꽃집 사장의 팁", "실전 팁" 등으로 대체
   - "---" 구분선 유지
   - "**강조**" 볼드 유지
   - "> 인용구" 유지
   - "• 항목" 목록 유지
   - 소제목에 번호 붙이지 않기 (1. 2. 3. 금지)
   - 본문 안에서도 "1. ", "2. ", "3. " 같은 번호 매기기 금지 — 대신 "• ", "이모지 " 형식 사용
   - 소제목은 반드시 3~5개 유지

9. 인트로(# 제목과 첫 ## 사이)는 짧은 줄 3~5줄로 유지
   - 한 줄에 한 문장만! 짧고 간결하게 (20~40자)
   - 긴 문장은 두 줄로 나누세요
   - "안녕하세요", 페르소나 이름으로 시작 금지

10. 글 마지막에 반드시 마무리 문장 1~2줄 유지

[초안]
{draft}`;

export async function retouchContent(input: RetouchInput): Promise<string> {
  const prompt = RETOUCH_PROMPT
    .replace("{persona}", input.persona)
    .replace("{keyword}", input.keyword)
    .replace("{draft}", input.draft);

  return callGemini(prompt, 8192);
}
